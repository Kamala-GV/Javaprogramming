package day_10;

public class Grade_Switch {
    public static void main(String[] args) {
        char ch ='C';
        switch(ch){
            case 'A':
                System.out.println("Excellent");
                break;
            case 'B':
                System.out.println("Great job");
                  break;
            case 'C':
                System.out.println("Good");
                break;
            case 'D':
                System.out.println("Passed");
                break;
            case 'F' :
                System.out.println("Failed");

        }
    }

}
